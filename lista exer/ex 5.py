#5) Faça um programa que leia um valor em metros e o exiba convertido em centímetros e milímetros

metros = float(input("Digite o valor em Metros para se obter em centímetro e milímetro: "))
cm = metros*100
ml = metros*1000
print("O valor em centímetro é : ",cm)
print("O valor em milímetro é : ",ml)